// Simple page generator demo
function createPage(name) {
  const content = `
    <!DOCTYPE html>
    <html>
    <head>
      <title>${name} - VibeScript</title>
      <link rel="stylesheet" href="assets/css/style.css">
    </head>
    <body>
      <header><h1>${name}</h1></header>
      <main><p>This page was generated by VibeScript Builder.</p></main>
    </body>
    </html>
  `;
  console.log("Generated page:", content);
  alert(`Page "${name}" created! (Check console for output)`);
}
